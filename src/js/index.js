var vetLita = [];


function salvarItem(){

    /** Não deixa a pagina atualizar após esse evento */
    event.preventDefault();
    /** let nome = document.querySelector('#nome').nodeValue; */
    let itenss = $ ('#Fake').text();
    let un = $ ('#UN').text();
    let quant = $ ('#qt').val();

    let itemLista = new Item (itenss,un,quant);

    vetLita.push(itemLista);

    renderizarTabelaLista();
    
}

function criaCabecalhoLista(){
 
    /** Como minha lista vai funcionar como uma tabela, estou criando a tabela abaixo */
    let cabecalho = document.createElement('thead');    /** Cria a primeira e principal linha da tabela */
    let linhaCabecalho = document.createElement('tr');  /** Cria a minha primeira linha da tabela */
    let usuario = document.createElement('th');
    usuario.innerText = ' ';
    let usuario1 = document.createElement('th');
    usuario1.innerText = ' ';
    let colunaItem = document.createElement('th');      /** Cria a coluna da tabela assim como as demais abaixo*/
    colunaItem.innerText = 'Itens';                     /** Coloca um texto na coluna assim como as demais abaixo */
    let colunaUni = document.createElement('th');
    colunaUni.innerText = 'Unidade';
    let colunaQuant = document.createElement('th');
    colunaQuant.innerText = 'Quantidade';

    /** Adiciona as colunas a linhaCabecalho */
    linhaCabecalho.appendChild(usuario);
    linhaCabecalho.appendChild(colunaItem);
    linhaCabecalho.appendChild(colunaUni);
    linhaCabecalho.appendChild(colunaQuant);
    linhaCabecalho.appendChild(usuario1);
    /** Adiciona a linhaCabecalho ao cabecalho */
    cabecalho.appendChild(linhaCabecalho);

    /** Essa função retorna apenas o cabeçalho da tebela */
    return cabecalho;
    
}

function criaCorpoLista(){

    /**Cria o corpo da tabela */
    let corpoTabela = document.createElement('tbody');

    /** Verifica se há pelo menos um item na lista */
    for(let i = 0; i < vetLita.length; i++){

        let linha = document.createElement('tr');
        
        /** Cria as celular da tabela e adiciona elas a linha da tabela */
        var img = document.createElement("img");
        img.src = "src/img/carrinho.png"
        linha.appendChild(img);

        let celulaItens = document.createElement('td');
        celulaItens.innerText = vetLita[i].itenss;
        linha.appendChild(celulaItens);

        let celulaUni = document.createElement('td');
        celulaUni.innerText = vetLita[i].un;
        linha.appendChild(celulaUni);

        let celulaQt = document.createElement('td');
        celulaQt.innerText = vetLita[i].quant;
        linha.appendChild(celulaQt);

        var img = document.createElement("img");
        img.src = "src/img/excluir.png";
        linha.appendChild(img);

        /** Adiciona a linha ao corpo da tabela */
        corpoTabela.appendChild(linha);

    }

    return corpoTabela;
}

function renderizarTabelaLista(){
    if(vetLita.length > 0){
        let areaListagemLista = document.getElementById('Lista');
        areaListagemLista.innerHTML = '';
        
        let tabela = document.createElement('table');

        let cabecalho = criaCabecalhoLista();
        tabela.appendChild(cabecalho);

        let corpoTabela = criaCorpoLista();
        tabela.appendChild(corpoTabela);

        areaListagemLista.appendChild(tabela);
    }
}

class Item{

    constructor(itenss, un, quant){
        this.itenss = itenss;
        this.un = un;
        this.quant = quant;
    }

    
}

